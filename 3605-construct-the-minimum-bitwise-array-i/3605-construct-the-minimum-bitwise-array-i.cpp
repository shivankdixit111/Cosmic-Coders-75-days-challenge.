class Solution {
public:
    vector<int> minBitwiseArray(vector<int>& nums) {
        vi ans;
         for(auto it:nums) {
            int a=-1;
             for(int mid=it-1;mid>=1;mid--) {

                if( (mid | (mid+1)) == it ) {
                    a = mid;
                }
             }
             ans.pb(a);
         }
         return ans;
    }
};